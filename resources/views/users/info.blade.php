<h3>このアプリの使い方</h3>
<br>
<p><span class="under_line">STEP1 　出来事把握</span><br>
   まず、落ち込んでしまった、<br>
   などのネガティブで考え方を変えたい出来事を思い出し記載します。<br>
   <br>
   <span class="under_line">STEP2　3コラム</span><br>
  その出来事を元にどのように考え、感じたのかを3コラムに分けて記入しましょう。<br>
  また、その考えに癖がないか確認してみましょう。
</p>

<p><span class="under_line">STEP3　7コラム</span><br>
  次に３コラム詳細ページから７コラムの作成ボタンをクリックして<br>
  7コラムを作成し、考え方を修正しましょう。<br>
  <br>
  客観的事実に基づいて検証し、<br>
  自分にとって好ましい認知へ修正しましょう。
</p>

