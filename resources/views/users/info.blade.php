<h3>このアプリの使い方</h3>
<br>
<p>出来事を元にどうように感じ、考えているかについて記載します。<br>
  落ち込んでしまった、などのネガティブな考え方を変えたいイベントを思い出し、<br>
  出来事を記入してみましょう。<br>
  その出来事を元にどのように考え、感じたのかを3コラムに分けて記入しましょう。<br>
  また、その考えに癖がないか確認してみましょう。
</p>
<p>1. 出来事<br>
   2. その時の感情<br>
   3. 考えたこと<br>
   3-1.考え方の癖<br>
</p>

<p>
  次に3コラム詳細ページから７コラムの作成ボタンをクリックして<br>
  考え方を修正しましょう。<br>
  <br>
  客観的事実に基づいて検証し、<br>
  自分にとって好ましい認知へ修正しましょう。
</p>

